-- Create a table for public profiles
create table profiles (
  id uuid references auth.users on delete cascade not null primary key,
  updated_at timestamp with time zone,
  username text unique,
  full_name text,
  phone_number text, -- 10 digit phone number (optional)
  avatar_url text,
  website text,

  constraint username_length check (char_length(username) >= 3)
);

-- Set up Row Level Security (RLS)
-- See https://supabase.com/docs/guides/auth/row-level-security for more details.
alter table profiles enable row level security;

create policy "Public profiles are viewable by everyone." on profiles
  for select using (true);

create policy "Users can insert their own profile." on profiles
  for insert with check (auth.uid() = id);

create policy "Users can update own profile." on profiles
  for update using (auth.uid() = id);

-- This triggers a profile creation when a user signs up. 
-- However, we are opting for a MANUAL profile creation via the Onboarding Modal 
-- because we need to enforce Unique Username which the robust signup form doesn't handle easily without custom functions.
-- So we will NOT add a trigger here. The app will handle the insert.
